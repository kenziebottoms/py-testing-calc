class Calculator():
    """Performs the four basic mathematical operations

    Methods:
        add(number, number)
        subtract(number, number)
        multiply(number, number)
        divide(number,number)
    """

    def add(self, x, y):
        """Adds two numbers together

        Arguments:
            x - Any number
            y - Any number
        """

        return x + y

    def subtract(self, x, y):
        """Subtracts the second number from the first

        Arguments:
            x - Any number
            y - Any number
        """

        return x - y
    
    def multiply(self, x, y):
        """Multiplies the two numbers together

        Arguments:
            x - Any number
            y - Any number
        """

        return x * y
    
    def divide(self, x, y):
        """Multiplies the first number by the second

        Arguments:
            x - Any number
            y - Any number
        """

        return x / y